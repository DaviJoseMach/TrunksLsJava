package JavaDojo.st.tierone;

public class Loops {
    public static void main (String [] args){
        // while, do while e for
        int count = 1;
        while(count <= 10){ // condição
            System.out.println(count);
            count++;
        }
        for(int i=0; i<15; i++){ // indices
            System.out.println(i);
        }
    }
}
