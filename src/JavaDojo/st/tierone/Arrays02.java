package JavaDojo.st.tierone;

public class Arrays02 {
    public static void main(String[] args) {
        String[] nomes = new String[4];
        nomes [0] = "Baki";
        nomes [1] = "Broly";
        nomes [2] = "Bispo";
        nomes [3] = "Bournout";

        for(int i = 0; i < nomes.length; i++){
            System.out.println(nomes[i]);
        }
    }
}
